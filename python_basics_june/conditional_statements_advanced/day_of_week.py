week_day = int(input())

if week_day == 1:
    print(f'Monday')
elif week_day == 2:
    print(f'Tuesday')
elif week_day == 3:
    print(f'Wednesday')
elif week_day == 4:
    print(f'Thursday')
elif week_day == 5:
    print(f'Friday')
elif week_day == 6:
    print(f'Saturday')
elif week_day == 7:
    print(f'Sunday')
else:
    print(f'Error')
